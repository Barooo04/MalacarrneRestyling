function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

const keywordRegex = new RegExp(`(${KEYWORDS.map(escapeRegex).join("|")})`, "gi");

function renderHighlightedText(text) {
  const labelMatch = text.match(/^([^:]{2,80}:)\s*(.*)$/);
  if (labelMatch) {
    return (
      <>
        <strong className="privacy-keyword">{labelMatch[1]}</strong>{" "}
        {renderHighlightedText(labelMatch[2])}
      </>
    );
  }

  const parts = text.split(keywordRegex);
  return parts.map((part, index) => {
    if (!part) return null;
    const isKeyword = KEYWORDS.some((keyword) => keyword.toLowerCase() === part.toLowerCase());
    if (!isKeyword) return <span key={`${part}-${index}`}>{part}</span>;
    return (
      <strong key={`${part}-${index}`} className="privacy-keyword">
        {part}
      </strong>
    );
  });
}

function parsePrivacyText(rawText) {
  const normalizedText = rawText
    .replace(/^INFORMATIVA SULLA PRIVACY\s*/m, "")
    .replace(/^ai sensi dell'art\.[^\n]*\n?/m, "")
    .trim();

  const paragraphs = normalizedText
    .split("\n\n")
    .map((paragraph) => paragraph.trim())
    .filter(Boolean);

  const blocks = [];

  paragraphs.forEach((paragraph) => {
    let lines = paragraph
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean);
    if (!lines.length) return;

    const sectionMatch = lines[0].match(/^(\d+\.\s+.+)$/);
    if (sectionMatch) {
      blocks.push({ type: "section", text: sectionMatch[1] });
      lines = lines.slice(1);
      if (!lines.length) return;
    }

    const subsectionMatch = lines[0].match(/^([a-z]\)\s+.+)$/i);
    if (subsectionMatch) {
      blocks.push({ type: "subsection", text: subsectionMatch[1] });
      lines = lines.slice(1);
      if (!lines.length) return;
    }

    const introLines = [];
    const bullets = [];
    let currentBullet = "";

    lines.forEach((line) => {
      if (/^[-•]\s+/.test(line)) {
        if (currentBullet) bullets.push(currentBullet.trim());
        currentBullet = line.replace(/^[-•]\s+/, "").trim();
        return;
      }

      if (currentBullet) {
        currentBullet = `${currentBullet} ${line}`;
        return;
      }

      introLines.push(line);
    });

    if (currentBullet) bullets.push(currentBullet.trim());

    if (introLines.length) {
      blocks.push({ type: "paragraph", text: introLines.join(" ") });
    }

    if (bullets.length) {
      blocks.push({ type: "list", items: bullets });
    }
  });

  return blocks;
}

